import { readdir, writeFile, readFile } from 'fs/promises'
import { basename, resolve } from 'path'
import { format, resolveConfig } from 'prettier'

// Directory of per-namespace English translation JSON (filename = i18n namespace).
const dirPath = resolve('src/i18n/en')

// Output: a first-class, checked-in source module. It owns the `Translations`
// namespace (one key interface per i18n namespace, browsable in the reference),
// the `Resources` map (`keyof Resources`, backs i18next `t()` typing), and the
// 'i18next' module augmentation. Regenerate with `npm run i18n:generate`.
const outputFilePath = resolve('src/i18n/types.d.ts')

// Default namespace
const defaultNamespace = 'common'

// Single-line, comment-safe representation of a default English string, for a
// `@defaultValue` TSDoc tag. JSON.stringify handles quotes/newlines; we further
// guard `*/` and backticks so the generated block comment can't be broken.
function escapeForTsDoc(value) {
  return JSON.stringify(value).replace(/\*\//g, '*\\/').replace(/`/g, '\\`')
}

// Render a translation object as interface members: leaf strings become `string`
// annotated with their English `@defaultValue`; nested objects recurse.
function generateKeys(source) {
  const members = []
  for (const key of Object.keys(source)) {
    if (typeof source[key] === 'string') {
      members.push(`/** @defaultValue \`${escapeForTsDoc(source[key])}\` */\n"${key}":string;`)
    } else {
      members.push(`"${key}":{\n${generateKeys(source[key])}\n};`)
    }
  }
  return members.join('\n')
}

async function main() {
  console.log(
    '‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚ēó   ‚Ėą‚Ėą‚ēó\n‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚Ėą‚Ėą‚ēó  ‚Ėą‚Ėą‚ēĎ\n‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĒ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚ēĎ\n‚Ėą‚Ėą‚ēĎ ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĎ\n‚Ėą‚Ėą‚ēĎ ‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĎ ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚ēĎ\n‚ēö‚ēź‚ēĚ ‚ēö‚ēź‚ēĚ ‚ēö‚ēź‚ēź‚ēź‚ēź‚ēĚ ‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēź‚ēź‚ēĚ',
  )

  // Sort for deterministic output ‚ÄĒ readdir order is filesystem-dependent, and a
  // nondeterministic member order would churn the checked-in file across machines
  // and flap the derive-check-i18n CI gate.
  const jsonFiles = (await readdir(dirPath)).filter(file => file.endsWith('.json')).sort()

  if (jsonFiles.length === 0) {
    throw new Error(`No translation JSON files found in ${dirPath}`)
  }

  // `Translations` is a namespace of per-namespace key interfaces (browsable as
  // `Translations.CompanyAddresses`); `Resources` is the map that references them
  // (`keyof Resources`, `Resources['Company.Addresses']`) and backs i18next
  // `t()` typing + `ResourceDictionary`.
  let namespaceMembers = ''
  let mapEntries = ''
  for (const file of jsonFiles) {
    const namespace = basename(file, '.json')
    const sanitizedName = namespace.replace(/\./g, '')
    const json = await readFile(resolve(dirPath, file), 'utf-8')
    mapEntries += `'${namespace}': Translations.${sanitizedName};\n`
    namespaceMembers += `/** Translation keys for the \`${namespace}\` i18n namespace. */\nexport interface ${sanitizedName}{\n${generateKeys(JSON.parse(json))}\n}\n`
  }

  const content = `
// This file is autogenerated
import 'i18next'

declare module 'i18next' {
  interface CustomTypeOptions {
    defaultNS: '${defaultNamespace}'
    resources: Resources
  }
}

/**
 * Every i18n resource namespace mapped to its keys. \`keyof Resources\` yields the
 * namespace names; \`Resources['Company.Addresses']\` (or {@link Translations.CompanyAddresses})
 * yields that namespace's keys. Backs i18next \`t()\` typing and \`ResourceDictionary\`.
 *
 * @public
 * @group Utility types
 */
export interface Resources {
${mapEntries}}

/**
 * All user-facing text rendered by SDK components, organized by namespace.
 * 
 * Every key includes its English default; pass overrides through a component's
 * \`dictionary\` prop or the global \`GustoProvider\` dictionary.
 *
 * @public
 */
export namespace Translations {
${namespaceMembers}}
`.trim()

  // Format with the repo's Prettier config so the committed file passes format:check.
  const prettierConfig = await resolveConfig(outputFilePath)
  const formatted = await format(content, { ...prettierConfig, parser: 'typescript' })

  await writeFile(outputFilePath, formatted, 'utf-8')
  console.log(`Generated i18n type declarations at ${outputFilePath}`)
}

main().catch(error => {
  console.error('Error generating i18n type declarations:', error)
  process.exit(1)
})
